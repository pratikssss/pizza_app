close all;
am=1;
ac=2;
ka=0.8;
fm=2;
fc=50;
fs=1000;
t=0:0.001:1;
mt=am*sin(2*pi*fm*t);
ct=ac*cos(2*pi*fc*t);
subplot(4,1,1);
plot(t,mt);
xlabel('time');
ylabel('m(t)');
title('message signal');
subplot(4,1,2);
plot(t,ct);
xlabel('time');
ylabel('c(t)');
title('carrier signal');
st=(1+ka*mt).*ct;
subplot(4,1,3);
plot(t,st);
xlabel('time');
ylabel('s(t)');
title('AM signal');
at=demod(st,fc,fs,"AM");
subplot(4,1,4);
plot(t,at);

